import java.util.*;
class mathOperations {
 public static void main(String[] args) {
  Scanner sc = new Scanner(System.in);
  System.out.println("Absolute of a number : "+(Math.abs(-989)));
  System.out.println("Ceil of a number : "+(Math.ceil(99.1)));
  System.out.println("Floor of a number : "+(Math.floor(99.1)));
  System.out.println("Round of a number : "+(Math.round(99.1)));
  System.out.println("Maximum of a number : "+(Math.max(89.6,89.8)));
  System.out.println("Minimum of a number : "+(Math.min(89.6,89.8)));
  System.out.println("Power 2 to the power of 100 : "+Math.pow(2,100));
  System.out.println("Square Root of a Number : "+Math.sqrt(785.645));
  System.out.println("Random of a number : "+Math.random());
  System.out.println("Exponential of a number : "+Math.exp(78.63));
  System.out.println("Logrithmic of a number : "+Math.log(100));
  System.out.println("Sine of a num : "+Math.sin(45));
  System.out.println("Cosine of a num : "+Math.cos(45));
  System.out.println("Value of Pi : "+Math.PI);
  sc.close();  
 }
}